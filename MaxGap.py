class Solution:
    # @param A : tuple of integers
    # @return an integer
    def maximumGap(self, A):
        if len(A) < 2: return 0

        maxVal = max(A)
        minVal = min(A)
        if maxVal == minVal: return 0

        n = len(A)
        gap = (maxVal - minVal) / float((n - 1))
        buckets = [(0, float('inf')) for _ in range(0, n)]
        for val in A:
            whichBucket = int((val - minVal) / gap)
            bMax, bMin = buckets[whichBucket]
            bMax = max(val, bMax)
            bMin = min(bMin, val)
            buckets[whichBucket] = (bMax, bMin)

        best = i = 0
        while i < len(buckets) - 1:
            bMax, _ = buckets[i]
            i += 1
            _, bMin = buckets[i]
            while bMin == float('inf'):
                i += 1
                if i > len(buckets) - 1:
                    return best
                _, bMin = buckets[i]
            best = max(best, bMin - bMax)
        return best


